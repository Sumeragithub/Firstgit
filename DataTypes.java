class DataTypes
{
	public static void main(String args[])
	{     
            byte value=4;
		short s=10000;
            int sem=8;
            long mobileNo=123456;
            float marks=90.7f;
            double salary=876542.99;
		char grade='O';
		boolean flag=true;

		System.out.println("Byte value:" +value);
		System.out.println("Short value:" +s);
		System.out.println("Integer value:" +sem);
		System.out.println("Long value:" +mobileNo);
		System.out.println("Float value:" +marks);
		System.out.println("Double value:" +salary);
		System.out.println("Character value:" +grade);
		System.out.println("Boolean value:" +flag);
         }
	}
